--------------------------------------------------------------------------------------------------------------------------------
-- GET TOPIC ATTRIBUTES
--------------------------------------------------------------------------------------------------------------------------------
-- Returns a list of attributes associated with a particular topic.
--------------------------------------------------------------------------------------------------------------------------------
CREATE PROCEDURE [dbo].[GetAttributes]
	@TopicID		INT	= -1
AS

--------------------------------------------------------------------------------------------------------------------------------
-- DECLARE AND SET VARIABLES
--------------------------------------------------------------------------------------------------------------------------------
SET NOCOUNT ON;

--------------------------------------------------------------------------------------------------------------------------------
-- SELECT ATTRIBUTES
--------------------------------------------------------------------------------------------------------------------------------
SELECT	AttributeKey,
	AttributeValue
FROM	Attributes
WHERE	TopicID		= @TopicID
ORDER BY	AttributeKey